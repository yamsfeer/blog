<h2 id="跳出率" tabindex="-1"><a class="header-anchor" href="#跳出率"><span>跳出率</span></a></h2>
<p>跳出率是一个网站的统计指标，表示访问者在浏览一个网页后立即离开的比例。通常用于衡量网站内容的吸引力和用户体验。</p>
