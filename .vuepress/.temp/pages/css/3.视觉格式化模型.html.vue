<template><div><h1 id="css-视觉格式化模型" tabindex="-1"><a class="header-anchor" href="#css-视觉格式化模型"><span>css 视觉格式化模型</span></a></h1>
<p>css 中一切皆盒子，视觉格式化模型 ( visual formatting model ) 就是决定这些盒子如何布局的一系列计算规则。</p>
<p>影响盒子布局的因素主要有：</p>
<ul>
<li>盒模型 ( box model )</li>
<li>定位方案 ( positioning scheme )</li>
<li>格式化上下文 ( formatting context )</li>
</ul>
<h2 id="盒模型" tabindex="-1"><a class="header-anchor" href="#盒模型"><span>盒模型</span></a></h2>
<p>盒模型可以分为以下两部分内容：</p>
<ul>
<li>盒模型：盒子的组成部分，包含 content、padding、border、margin</li>
<li>盒类型：盒子的类型决定了盒子的排布规则</li>
</ul>
<h2 id="定位方案" tabindex="-1"><a class="header-anchor" href="#定位方案"><span>定位方案</span></a></h2>
<p>定位方案有以下几种：</p>
<ul>
<li>常规流 ( flow )</li>
<li>浮动 ( float )</li>
<li>绝对定位 ( absolute positioning )</li>
</ul>
<h2 id="格式化上下文" tabindex="-1"><a class="header-anchor" href="#格式化上下文"><span>格式化上下文</span></a></h2>
<p>格式化上下文有以下几种：</p>
<ul>
<li>block formatting context ( BFC, 块级格式化上下文 )</li>
<li>inline formatting context ( IFC, 行内格式化上下文 )</li>
<li>flex formatting context ( FFC, 自适应格式化上下文 )</li>
<li>grid layout formatting context ( GFC, 网格布局格式化上下文 )</li>
</ul>
